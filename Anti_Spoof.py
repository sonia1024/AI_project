import os, time, copy, random
from collections import Counter
import numpy as np
import matplotlib.pyplot as plt

import torch
import torch.nn as nn
from torch.utils.data import DataLoader, WeightedRandomSampler
from torchvision import datasets, transforms, models
from torchvision.models import EfficientNet_B0_Weights

SEED = 42
random.seed(SEED); np.random.seed(SEED)
torch.manual_seed(SEED); torch.cuda.manual_seed_all(SEED)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("PyTorch:", torch.__version__, "| Device:", device)

"""

**하이퍼파라미터 설정**"""

DATA_DIR        = "./dataset"
IMG_SIZE        = 224
BATCH_SIZE      = 32
EPOCHS          = 15
LR              = 1e-4
WEIGHT_DECAY    = 1e-4
LABEL_SMOOTHING = 0.1
NUM_WORKERS     = 2
IMBALANCE       = "sampler"     # "sampler" | "loss_weight" | "none"
WEIGHTS_PATH    = "./best_model.pth"

IMAGENET_MEAN = [0.485, 0.456, 0.406]
IMAGENET_STD  = [0.229, 0.224, 0.225]

"""**데이터 불러오기 및 변환**"""

train_tf = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.RandomHorizontalFlip(p=0.5),
    transforms.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.1),
    transforms.RandomRotation(degrees=8),
    transforms.ToTensor(),
    transforms.Normalize(IMAGENET_MEAN, IMAGENET_STD),
])
eval_tf = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize(IMAGENET_MEAN, IMAGENET_STD),
])

train_ds = datasets.ImageFolder(os.path.join(DATA_DIR, "train"), train_tf)
val_ds   = datasets.ImageFolder(os.path.join(DATA_DIR, "val"),   eval_tf)
test_path = os.path.join(DATA_DIR, "test")
test_ds = datasets.ImageFolder(test_path, eval_tf) if os.path.isdir(test_path) else None

classes = train_ds.classes
print("클래스 매핑:", dict(enumerate(classes)))
print("train:", len(train_ds), "| val:", len(val_ds),
      "| test:", len(test_ds) if test_ds else "없음")

"""**데이터 불균형 처리**"""

train_targets = [label for _, label in train_ds.samples]
counts = Counter(train_targets)

sampler, shuffle, class_weights_tensor = None, True, None

if IMBALANCE == "sampler":
    per_class_w = {c: 1.0/n for c, n in counts.items()}
    sample_weights = [per_class_w[t] for t in train_targets]
    sampler = WeightedRandomSampler(torch.DoubleTensor(sample_weights),
                                    len(sample_weights), replacement=True)
    shuffle = False
    print("→ WeightedRandomSampler 사용")
elif IMBALANCE == "loss_weight":
    total, n_cls = sum(counts.values()), len(counts)
    w = [total/(n_cls*counts[i]) for i in range(n_cls)]
    class_weights_tensor = torch.FloatTensor(w).to(device)
    print("→ loss weight:", {classes[i]: round(w[i],3) for i in range(n_cls)})
else:
    print("→ 불균형 처리 없음")

train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, sampler=sampler,
                          shuffle=shuffle, num_workers=NUM_WORKERS, pin_memory=True)
val_loader   = DataLoader(val_ds, batch_size=BATCH_SIZE, shuffle=False,
                          num_workers=NUM_WORKERS, pin_memory=True)
test_loader  = (DataLoader(test_ds, batch_size=BATCH_SIZE, shuffle=False,
                           num_workers=NUM_WORKERS, pin_memory=True) if test_ds else None)
print("DataLoader 준비 완료")

"""**모델 설정**"""

def build_model(num_classes=3, freeze_backbone=False):
    model = models.efficientnet_b0(weights=EfficientNet_B0_Weights.IMAGENET1K_V1)
    if freeze_backbone:
        for p in model.parameters():
            p.requires_grad = False
    in_features = model.classifier[1].in_features
    model.classifier[1] = nn.Linear(in_features, num_classes)
    return model

model = build_model(num_classes=len(classes)).to(device)
criterion = nn.CrossEntropyLoss(weight=class_weights_tensor,
                                label_smoothing=LABEL_SMOOTHING)
optimizer = torch.optim.AdamW(model.parameters(), lr=LR, weight_decay=WEIGHT_DECAY)
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=EPOCHS)
print(model.classifier)

"""**모델 학습**"""

def run_epoch(model, loader, criterion, optimizer, train=True):
    model.train() if train else model.eval()
    total_loss, correct, total = 0.0, 0, 0
    torch.set_grad_enabled(train)
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)
        if train: optimizer.zero_grad()
        outputs = model(images)
        loss = criterion(outputs, labels)
        if train:
            loss.backward(); optimizer.step()
        total_loss += loss.item() * images.size(0)
        correct += (outputs.argmax(1) == labels).sum().item()
        total += images.size(0)
    torch.set_grad_enabled(True)
    return total_loss/total, correct/total

history = {"train_loss":[], "train_acc":[], "val_loss":[], "val_acc":[]}
best_val_acc, best_state = 0.0, copy.deepcopy(model.state_dict())

for epoch in range(1, EPOCHS+1):
    t0 = time.time()
    tr_loss, tr_acc = run_epoch(model, train_loader, criterion, optimizer, True)
    va_loss, va_acc = run_epoch(model, val_loader,   criterion, optimizer, False)
    scheduler.step()
    for k, v in zip(history, [tr_loss, tr_acc, va_loss, va_acc]):
        history[k].append(v)
    print(f"[{epoch:2d}/{EPOCHS}] train_loss={tr_loss:.4f} acc={tr_acc:.4f} | "
          f"val_loss={va_loss:.4f} acc={va_acc:.4f} | {time.time()-t0:.1f}s")
    if va_acc > best_val_acc:
        best_val_acc = va_acc
        best_state = copy.deepcopy(model.state_dict())
        torch.save({"model_state": best_state, "classes": classes,
                    "img_size": IMG_SIZE}, WEIGHTS_PATH)
        print(f"   ↑ best 갱신 (val_acc={va_acc:.4f}) → 저장")

print(f"\n학습 완료. best val_acc = {best_val_acc:.4f}")

"""**학습 추이 시각화**"""

fig, ax = plt.subplots(1, 2, figsize=(12,4))
ax[0].plot(history["train_loss"], label="train"); ax[0].plot(history["val_loss"], label="val")
ax[0].set_title("Loss"); ax[0].set_xlabel("epoch"); ax[0].legend()
ax[1].plot(history["train_acc"], label="train"); ax[1].plot(history["val_acc"], label="val")
ax[1].set_title("Accuracy"); ax[1].set_xlabel("epoch"); ax[1].legend()
plt.tight_layout(); plt.show()

