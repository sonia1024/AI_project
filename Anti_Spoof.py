import torch
import torch.nn.functional as F
import numpy as np
import matplotlib.pyplot as plt
from PIL import Image
import glob, random, os

SAVE_DIR = "/content/drive/MyDrive/fas_project"   # 드라이브 안 쓰면 "."
os.makedirs(SAVE_DIR, exist_ok=True)

# ── 0) 모델이 메모리에 없으면 로드 ──────────────────────────
if "model" not in globals():
    from torchvision import models, transforms
    import torch.nn as nn
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    ckpt = torch.load(f"{SAVE_DIR}/best_model.pth", map_location=device)
    classes, IMG_SIZE = ckpt["classes"], ckpt["img_size"]
    model = models.efficientnet_b0(weights=None)
    model.classifier[1] = nn.Linear(model.classifier[1].in_features, len(classes))
    model.load_state_dict(ckpt["model_state"])
    model = model.to(device).eval()
    eval_tf = transforms.Compose([
        transforms.Resize((IMG_SIZE, IMG_SIZE)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
    ])

# ── 1) Grad-CAM 구현 ───────────────────────────────────────
class GradCAM:
    def __init__(self, model, target_layer):
        self.model = model
        self.activations = None   # forward 시 feature map 저장
        self.gradients = None     # backward 시 gradient 저장
        target_layer.register_forward_hook(self._save_activation)
        target_layer.register_full_backward_hook(self._save_gradient)

    def _save_activation(self, module, inp, out):
        self.activations = out.detach()

    def _save_gradient(self, module, grad_in, grad_out):
        self.gradients = grad_out[0].detach()

    def __call__(self, x, class_idx=None):
        out = self.model(x)                      # 순전파
        if class_idx is None:
            class_idx = int(out.argmax(1))       # 예측 클래스 기준
        self.model.zero_grad()
        out[0, class_idx].backward()             # 해당 클래스 점수로 역전파

        # 채널별 gradient 평균 = 채널 중요도(weight)
        weights = self.gradients.mean(dim=(2, 3), keepdim=True)
        cam = (weights * self.activations).sum(dim=1).squeeze()
        cam = F.relu(cam)
        cam = cam / (cam.max() + 1e-8)           # 0~1 정규화
        return cam.cpu().numpy(), class_idx, torch.softmax(out, 1)[0].detach().cpu().numpy()

# EfficientNet-B0의 마지막 conv 블록에 hook
cam_extractor = GradCAM(model, model.features[-1])

# ── 2) 역정규화(이미지 복원용) ──────────────────────────────
def denorm(t):
    mean = np.array([0.485, 0.456, 0.406]); std = np.array([0.229, 0.224, 0.225])
    img = t.cpu().numpy().transpose(1, 2, 0) * std + mean
    return np.clip(img, 0, 1)

# ── 3) 각 클래스에서 1장씩 골라 Grad-CAM 시각화(AI 활용하여 코드 생성) ─────────────
cls_order = ["real", "print", "replay"]
picks = [(random.choice(glob.glob(f"./dataset/test/{c}/*")), c) for c in cls_order]

fig, axes = plt.subplots(2, len(picks), figsize=(5 * len(picks), 9))
for col, (path, true_label) in enumerate(picks):
    img = Image.open(path).convert("RGB")
    x = eval_tf(img).unsqueeze(0).to(device)

    cam, pred_idx, probs = cam_extractor(x)
    pred_label = classes[pred_idx]
    cam_resized = np.array(Image.fromarray((cam * 255).astype(np.uint8))
                           .resize((IMG_SIZE, IMG_SIZE))) / 255.0
    base = denorm(x[0])

    # (위) 원본
    axes[0, col].imshow(base)
    axes[0, col].set_title(f"GT: {true_label}", fontsize=12, fontweight="bold")
    axes[0, col].axis("off")

    # (아래) 원본 + 히트맵 오버레이
    axes[1, col].imshow(base)
    axes[1, col].imshow(cam_resized, cmap="jet", alpha=0.5)
    ok = (pred_label == true_label)
    color = "#2E8B57" if ok else "#D62728"
    axes[1, col].set_title(f"Grad-CAM\nPred: {pred_label} ({probs[pred_idx]*100:.1f}%)",
                           fontsize=11, color=color, fontweight="bold")
    axes[1, col].axis("off")

plt.suptitle("Grad-CAM: where the model looks", y=1.01, fontsize=14)
plt.tight_layout()
